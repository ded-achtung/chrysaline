    print(f"  ✓ Быстро (<5мин): {ok_fast} ({elapsed:.1f}с)")

    ok = ok_not_exploded and ok_has_abstractions and ok_fast
    print(f"\n  {'═══ МАСШТАБ РАБОТАЕТ! ═══' if ok else '═══ НУЖНА ДОРАБОТКА ═══'}")
