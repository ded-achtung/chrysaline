def parse_text_to_sentences(text):
    text = re.sub(r'[—–]', ' ', text)
    text = re.sub(r'[!?]', '.', text)
    sentences = []
    for line in text.split('.'):
        line = line.strip().lower()
        words = [w.strip(',:;()«»""\n\t ') for w in line.split()]
        words = [w for w in words if w and len(w) >= 1]
        if len(words) >= 2:
            if len(words) <= 8:
                sentences.append(words)
            else:
                for i in range(0, len(words) - 1, 4):
                    chunk = words[i:i + 6]
                    if len(chunk) >= 2:
                        sentences.append(chunk)
    return sentences
